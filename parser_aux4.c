#include "Libft/libft.h"
#include "minishell.h"

int	is_special_char(char c)
{
	if (c == '`' || c == '(' || c == ')' || c == '{' || c == '}')
		return (1);
	if (c == '&' || c == ';' || c == '!' || c == '?' || c == '*')
		return (1);
	if (c == '#' || c == '~' || c == '\\')
		return (1);
	return (0);
}